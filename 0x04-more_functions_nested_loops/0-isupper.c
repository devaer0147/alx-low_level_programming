/**
 * _isupper - isupper function
 * @c: operand to be checked
 *
 * Return: 1 if c is uppercase otherwise 0
 * Description: function to check if operand c is uppercase or not
 */
int _isupper(int c)
{
	if (isupper(c))
	{
		return (1);
	}
	else
	{
		return (0);
	}
	return (0);
}
